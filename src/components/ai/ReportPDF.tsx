"use client";

/**
 * ReportPDF.tsx
 *
 * Renders the parsed report as a @react-pdf/renderer Document.
 *
 * - Zero DOM dependency
 * - Zero html2canvas dependency
 * - Zero Tailwind / CSS color parsing
 * - All colors are plain HEX literals
 * - Pagination is content-driven (never forced)
 */

import {
  Document,
  Page,
  Text,
  View,
  StyleSheet,
  Font,
} from '@react-pdf/renderer';
import { parseReport, ReportSection, ReportLine } from '@/services/reportParser';

