// ─── Sub-components ──────────────────────────────────────────────────────────

function CoverPage({ date }: { date: string }) {
  return (
    <Page size="A4" style={S.page}>
      <View style={S.coverWrapper}>
        <Text style={S.coverTitle}>DIGITAL SKILLS FOR DECENT WORK</Text>
        <View style={S.coverDivider} />
        <Text style={S.coverSubtitle}>Executive Research Report</Text>
        <Text style={S.coverMeta}>Port Harcourt, Rivers State, Nigeria</Text>
        <Text style={S.coverMeta}>{date}</Text>
        <Text style={S.coverMeta}>Generated by AI Data Analyst</Text>
        <Text style={S.coverSDG}>
          Aligned with UN Sustainable Development Goals 8 (Decent Work) &amp; 9 (Industry, Innovation and Infrastructure)
        </Text>
      </View>
      <PageFooter date={date} />
    </Page>
  );
}

function PageFooter({ date }: { date: string }) {
  return (
    <View style={S.footer} fixed>
      <Text style={S.footerLeft}>Digital Skills for Decent Work — {date}</Text>
      <Text
        style={S.footerRight}
        render={({ pageNumber, totalPages }) =>
          `Page ${pageNumber} of ${totalPages}`
        }
      />
    </View>
  );
}

function SectionBlock({ section }: { section: ReportSection }) {
  return (
    <View style={S.sectionWrapper} wrap={false}>
      <View style={S.sectionHeaderBar}>
        <Text style={S.sectionNumber}>{section.number}.</Text>
        <Text style={S.sectionTitle}>{section.title}</Text>
      </View>
      <SectionBody lines={section.lines} />
    </View>
  );
}

function SectionBody({ lines }: { lines: ReportLine[] }) {
  return (
    <>
      {lines.map((line, i) => {
        if (line.type === 'blank') return <View key={i} style={{ height: 6 }} />;
        if (line.type === 'bullet') {
          return (
            <View key={i} style={S.bulletRow}>
              <Text style={S.bulletDot}>•</Text>
              <Text style={S.bulletText}>{line.content}</Text>
            </View>
          );
        }
        return (
          <Text key={i} style={S.bodyText}>{line.content}</Text>
        );
      })}
    </>
  );
}

// ─── Main Document ────────────────────────────────────────────────────────────

interface ReportPDFDocumentProps {
  report: string;
  date: string;
}

export function ReportPDFDocument({ report, date }: ReportPDFDocumentProps) {
  const { sections } = parseReport(report);

  // Pack sections onto content pages, 3 sections per page max to avoid overflow
  // @react-pdf/renderer handles page breaks automatically via `wrap` — we just
  // need to group them into pages.
  return (
    <Document
      title="Digital Skills for Decent Work"
      author="AI Data Analyst"
      subject="Digital Readiness Survey Report — Port Harcourt"
    >
      {/* Cover page */}
      <CoverPage date={date} />

      {/* Content page(s) — let renderer paginate automatically */}
      <Page size="A4" style={S.page}>
        {sections.map((section) => (
          <SectionBlock key={section.number} section={section} />
        ))}
        <PageFooter date={date} />
      </Page>
    </Document>
  );
}
